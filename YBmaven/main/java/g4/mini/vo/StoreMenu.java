package g4.mini.vo;

public class StoreMenu {
	private int stoNo;
	private String stoMenu;
	private int price;
	private int calorie;
	
	public int getStoNo() {
		return stoNo;
	}
	public void setStoNo(int stoNo) {
		this.stoNo = stoNo;
	}
	public String getStoMenu() {
		return stoMenu;
	}
	public void setStoMenu(String stoMenu) {
		this.stoMenu = stoMenu;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getCalorie() {
		return calorie;
	}
	public void setCalorie(int calorie) {
		this.calorie = calorie;
	}
}
